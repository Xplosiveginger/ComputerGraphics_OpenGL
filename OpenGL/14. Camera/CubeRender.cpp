void RenderCube(Shader myShader, float color1[4], float color2[4], float mul, glm::vec2 resolution, Camera camera)
{
	shaderID = myShader.ID;

	glBindVertexArray(VAO);

	glm::mat4 view = glm::mat4(1.0f);
	glm::mat4 projection = glm::mat4(1.0f);
	//view = glm::translate(view, glm::vec3(0.0f, 0.0f, -3.0f));

	/*glm::mat4 view;*/
	const float radius = 10.0f;
	float camX = sin(glfwGetTime()) * radius;
	float camZ = cos(glfwGetTime()) * radius;

	view = glm::lookAt(camera.Position, camera.Position + camera.Front, camera.Up);

	projection = glm::perspective(glm::radians(camera.Zoom), resolution.x/ resolution.y, 0.1f, 100.0f);

	myShader.use();

	// Pass the time to the shader for potential animated effects
	float timeValue = glfwGetTime();
	myShader.setFloat("time", timeValue);

	for (unsigned int i = 0; i < 10; i++) {
		// Create transformations
		glm::mat4 model = glm::mat4(1.0f);
		
		model = glm::translate(model, cubePositions[i]);
		model = glm::rotate(model, (float)glfwGetTime(), glm::vec3(0.5f, 1.0f, 0.0f));

		myShader.setMat4("model", model);

		
		glDrawArrays(GL_TRIANGLES, 0, 36);
	}

	myShader.setMat4("view", view);
	myShader.setMat4("projection", projection);
	myShader.setVec4("color1", color1);
	myShader.setVec4("color2", color2);
	myShader.setFloat("mul", mul);
}

void DeleteCubeData()
{
	glDeleteVertexArrays(1, &VAO);
	glDeleteBuffers(1, &VBO);
	glDeleteProgram(shaderID);
}